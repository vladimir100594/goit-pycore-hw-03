# Імпортуємо модуль random для генерації випадкових чисел
import random

# Функція для генерації унікальних випадкових чисел для лотереї
def get_numbers_ticket(min, max, quantity):
    # перевіряємо базові обмеження: min >= 1, max <= 1000, quantity > 0
    if min < 1 or max > 1000 or quantity < 1:
        return [] # Повертаємо пустий список якщо параметри некоректні
    # Перевіряємо щоб min не був більше max (логічна помилка)
    if min > max:
        return [] # Неможливо вибрати числа якщо початок більше кінця
    # Перевіряємо чи не просимо більше чисел ніж є в діапазоні
    # Наприклад: діапазон 1-10 містить 10 чисел, не можна вибрати 15
    if quantity > (max - min + 1):
        return []  # Неможливо вибрати більше унікальних чисел ніж існує
    # Створюємо множину для зберігання унікальних чисел
    numbers = set()
    # Генеруємо випадкові числа поки не наберемо потрібну кількість
    while len(numbers) < quantity:
        # Генеруємо випадкове число в заданому діапазоні (включно)
        random_number = random.randint(min, max)
        # Додаємо до множини (автоматично ігнорує дублікати)
        numbers.add(random_number)
     # перетворюємо множену в список та сортуємо за зростанням
    result = sorted(list(numbers))
    # повертаємо відсортований список унікальних чисел
    return result
if __name__ == "__main__":
    # тест 1: стандартна лотерея 6 з 49
    print("Лотерея 6 з 49:", get_numbers_ticket(1, 49, 6))
    # тест 2: мала лотерея 5 з 36
    print("Лотерея 5 з 36:", get_numbers_ticket(1, 36, 5))
    # тест 3: неправильні параметри (максимум менше мінімуму)
    print("Неправильні параметри:", get_numbers_ticket(10, 5, 3))
