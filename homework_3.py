# Імпортуємо модуль re для роботи з регулярними виразами
import re

# Функція для нормалізації телефонних номерів до стандартного формату
def normalize_phone(phone_number):
    # Перевіряємо чи параметр є рядком
    if not isinstance(phone_number, str):
        return ""  # Повертаємо пустий рядок для некоректного типу
    # Видаляємо всі символи крім цифр
    digits = re.sub(r'\D', '', phone_number)
    # Перевіряємо довжину отриманого рядка з цифрами
    if len(digits) == 10:
        # Якщо 10 цифр, вважаємо що це номер без коду країни, додаємо +38
        return "+38" + digits
    elif len(digits) == 12 and digits.startswith('38'):
        # Якщо 12 цифр і починається з 38, додаємо +
        return "+" + digits
    elif len(digits) == 11 and digits.startswith('0'):
        # Якщо 11 цифр і починається з 0, замінюємо 0 на +38
        return "+38" + digits[1:]
    elif len(digits) == 13 and digits.startswith('380'):
        # Якщо 13 цифр і починається з 380, додаємо +
        return "+" + digits
    else:
        # Всі інші випадки вважаємо некоректними
        return ""  # Повертаємо пустий рядок для некоректного формату
# Тестування функції з різними параметрами
if __name__ == "__main__":
    raw_numbers = [
        "067\\t123 4567",
        "(095) 234-5678\\n",
        "+380 44 123 4567",
        "380501234567",
        "    +38(050)123-32-34",
        "     0503451234",
        "(050)8889900",
        "38050-111-22-22",
        "38050 111 22 11   ",
        "abracadabra",
    ]
    # Нормалізуємо і виводимо результати
    sanitized_numbers = [normalize_phone(num) for num in raw_numbers]
    print("Нормалізовані номери телефонів:", sanitized_numbers)
